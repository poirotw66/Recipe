function patch(locale, slug, steps) {
  const file = join(root, `src/content/${locale}/${slug}.md`);
  const lines = readFileSync(file, "utf8").replace(/\r\n/g, "\n").split("\n");
  const start = lines.findIndex((line) => line === "steps:");
  if (start < 0) throw new Error(`No steps: in ${file}`);
  let end = start + 1;
  while (end < lines.length && lines[end].startsWith("- ")) end++;
  const block = ["steps:", ...steps.map((step) => `- ${step}`)];
  const out = [...lines.slice(0, start), ...block, ...lines.slice(end)].join("\n");
  writeFileSync(file, out.replace(/^updatedAt:.*$/m, `updatedAt: "${UPDATED}"`), "utf8");
  console.log(`OK ${locale}/${slug}`);
}

const slugs = process.argv.slice(2);
const targets = slugs.length ? slugs : Object.keys(STEPS);

for (const slug of targets) {
  const entry = STEPS[slug];
  if (!entry) {
    console.error(`No STEPS entry for ${slug}`);
    continue;
  }
  patch("recipes-ja", slug, entry.ja);
  patch("recipes-ko", slug, entry.ko);
}
